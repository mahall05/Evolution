package Maps;

public class Goal{
    public int x = 0;
    public int y = 0;
    public int width = 0;
    public int height = 0;
    public Goal(int x, int y, int width, int height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
